class TestParseReportSections:
    def test_parse_incident_report(self):
        text = (
            "--- INCIDENT REPORT SUMMARY ---\n"
            "Generated by AskAdil\n\n"
            "WHAT HAPPENED:\nSomeone shouted Islamophobic abuse.\n\n"
            "WHERE THIS HAPPENED:\nBirmingham city centre.\n\n"
            "WHEN THIS HAPPENED:\n15 March 2026, around 3pm.\n\n"
            "LEGAL CONTEXT:\nCrime and Disorder Act 1998 s.28.\n\n"
            "--- END REPORT ---"
        )
        sections = parse_report_sections(text)
        assert len(sections) >= 3
        assert sections[0].heading == "WHAT HAPPENED"
        assert "shouted" in sections[0].content

    def test_parse_solicitor_pack(self):
        text = (
            "--- SOLICITOR CONSULTATION PACK ---\n\n"
            "YOUR SITUATION:\nDenied prayer breaks at work.\n\n"
            "KEY DATES:\nIncident: 10 March 2026\n\n"
            "RELEVANT LEGISLATION:\nEquality Act 2010 s.13\n\n"
            "WHAT TO ASK YOUR SOLICITOR:\n1. Do I have grounds?\n\n"
            "--- END PACK ---"
        )
        sections = parse_report_sections(text)
        assert len(sections) >= 3
        assert any(s.heading == "KEY DATES" for s in sections)
        assert any(s.heading == "RELEVANT LEGISLATION" for s in sections)

    def test_parse_empty_text(self):
        sections = parse_report_sections("")
        assert sections == []

    def test_parse_no_sections(self):
        sections = parse_report_sections("Just some random text without headings.")
        assert sections == []
