    def test_defaults_to_england_wales(self):
        prompt = _build_solicitor_pack_prompt(None)
        assert "solicitors.lawsociety.org.uk" in prompt

    def test_is_instruction_not_conversation(self):
        prompt = _build_solicitor_pack_prompt()
        assert "NOT having a conversation" in prompt


